#include "3-calc.h"
/**
 * op_add - adds
 * @a: the first
 * @b: the second
 * Return: the restult;
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - adds
 * @a: the first
 * @b: the second
 * Return: the restult;
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - adds
 * @a: the first
 * @b: the second
 * Return: the restult;
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - adds
 * @a: the first
 * @b: the second
 * Return: the restult;
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - adds
 * @a: the first
 * @b: the second
 * Return: the restult;
 */
int op_mod(int a, int b)
{
	return (a % b);
}
